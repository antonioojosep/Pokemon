    public function getRandomPokemon(): ?Pokemon
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql = 'SELECT * FROM pokemon ORDER BY RAND() LIMIT 1';
        
        try {
            $result = $conn->executeQuery($sql)->fetchAssociative();
            
            if (!$result) {
                return null;
            }
            
            return $this->find($result['id']);
        } catch (\Exception $e) {
            return null;
        }
    }
